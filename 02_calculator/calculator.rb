def add (num1, num2)
    (num1 + num2)

end

def subtract (num1, num2)
    (num1 - num2)

end

def sum (array)
    result = 0
    i = 0
    if array.length == 0
        0
    elsif
        while i < array.length do
            result += array[i]
            i += 1
        end
    end
    result
end

def multiply (num1, num2)
    num1 * num2
end
